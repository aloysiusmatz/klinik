     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        /*build message*/
        if($request->session()->has('s_login')){
            $msg_type = 'none';
            $message = '-';
        }else{
            $request->session()->put('s_login','1');
            
            $message = Auth::user()->name;
            $message = 'Welcome, '.$message;
            $msg_type = 'success';
        }

        return view('pages.settings.pages')
                            ->with('notif_type', $msg_type)
                            ->with('notif_msg' , $message);
    }

